package Components;

public class DVDDrive {
    private String name;
    private int price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    public DVDDrive(){
        name="DVD Drive";
        price=6000;
    }
}
